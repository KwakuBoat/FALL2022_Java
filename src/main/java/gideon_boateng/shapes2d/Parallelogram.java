package main.java.gideon_boateng.shapes2d;

/**
 * Applied to rectangles and squares in two dimension
 */

public abstract class Parallelogram {
    /*
     method may apply to a variety of non-circular
     */
    public abstract double getArea();
    /**
     * length data member common to all parallelogram
     */
    protected double length;
    /**
     * width data number common to all parallelogram
     */
    protected double width;



}
